'''
Question:

Write a program to check whether a number is even or odd using switch case.
Input
First Line of the input contains the number n.

Constraints
1 <= n <= 1e9
Output
If the number is even print "Even" otherwise "Odd"
Example

Solution as Follows:
'''
num=int(input())
if num%2==0:
 print("Even")
else:
 print("Odd")